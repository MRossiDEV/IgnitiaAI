export function createLeadFromWizard(data: any): Lead {
  const lead: Lead = {
    id: `lead_${Date.now()}`,
    partnerId: "platform",
    dealId: "audit_free",
    name: data.businessName || "Unknown Business",
    email: data.email || "unknown@email.com",
    phone: data.phone,
    website: data.website,
    industry: data.industry,
    status: "new",
    source: "audit",
    estimatedValue: 500,
    createdAt: new Date().toISOString(),
    updatedAt: new Date().toISOString(),
    notes: data.painPoints,
  }

  leads.push(lead)
  return lead
}

